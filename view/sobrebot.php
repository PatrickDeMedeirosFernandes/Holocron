<div id='bodybox'>
    <h4>O que é ChatterBot ou ChatBot</h4>
    <p>
        Chatbot (ou chatterbot) é um programa de computador que tenta simular um ser humano na conversação com as pessoas. 
        O objetivo é responder as perguntas de tal forma que as pessoas tenham a impressão de estar conversando com outra 
        pessoa e não com um programa de computador.
        Após o envio de perguntas em linguagem natural, o programa consulta uma base de conhecimento e em seguida fornece
        uma resposta que tenta imitar o comportamento humano.
    </p>
